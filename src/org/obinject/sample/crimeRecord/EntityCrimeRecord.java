    @Override
    public boolean pullEntity(byte[] array, int position) {
        PullPage pull = new PullPage(array, position);
        Uuid storedClass = pull.pullUuid();
        if (classId.equals(storedClass) == true) {
            this.uuid = pull.pullUuid();
            this.setID(pull.pullInteger());
            this.setCaseNumber(pull.pullString());
            this.setBlock(pull.pullString());
            this.setIUCR(pull.pullString());
            this.setLocationDescription(pull.pullShort());
            this.setArrest(pull.pullBoolean());
            this.setCommunityArea(pull.pullByte());
            this.setXCoord(pull.pullLong());
            this.setYCoord(pull.pullLong());
            this.setLatitude(pull.pullDouble());
            this.setLongitude(pull.pullDouble());
            return true;
        }
        return false;
    }

    @Override
    public void pushEntity(byte[] array, int position) {
        PushPage push = new PushPage(array, position);
        push.pushUuid(classId);
        push.pushUuid(uuid);
        push.pushInteger(this.getID());
        push.pushString(this.getCaseNumber());
        push.pushString(this.getBlock());
        push.pushString(this.getIUCR());
        push.pushShort(this.getLocationDescription());
        push.pushBoolean(this.isArrest());
        push.pushByte(this.getCommunityArea());
        push.pushLong(this.getXCoord());
        push.pushLong(this.getYCoord());
        push.pushDouble(this.getLatitude());
        push.pushDouble(this.getLongitude());
    }

    @Override
    public int sizeOfEntity() {
        return 2 * Page.sizeOfUuid
                + Page.sizeOfInteger //ID
                + Page.sizeOfString(this.getCaseNumber()) //Case Number
                + Page.sizeOfString(this.getBlock()) //Block
                + Page.sizeOfString(this.getIUCR()) //IUCR
                + Page.sizeOfShort //Location Description
                + Page.sizeOfBoolean //Arrest
                + Page.sizeOfByte // Community Area
                + Page.sizeOfLong //X Coordinate
                + Page.sizeOfLong // Y Coordinate
                + Page.sizeOfDouble //Latitude
                + Page.sizeOfDouble;   //Longitude
    }

