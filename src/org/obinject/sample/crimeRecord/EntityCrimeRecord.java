    public static final double PRECISION = 0.00000001;

    public EntityCrimeRecord() {
        this.uuid = Uuid.generator();
    }

    public EntityCrimeRecord(CrimeRecord record, Uuid uuid) {
        this.setID(record.getID());
        this.setCaseNumber(record.getCaseNumber());
        this.setBlock(record.getBlock());
        this.setIUCR(record.getIUCR());
        this.setLocationDescription(record.getLocationDescription());
        this.setArrest(record.isArrest());
        this.setCommunityArea(record.getCommunityArea());
        this.setXCoord(record.getXCoord());
        this.setYCoord(record.getYCoord());
        this.setLatitude(record.getLatitude());
        this.setLongitude(record.getLongitude());
        this.uuid = new Uuid(uuid.getMostSignificantBits(), uuid.getLeastSignificantBits());
    }

    public EntityCrimeRecord(EntityCrimeRecord entity) {
        this.setID(entity.getID());
        this.setCaseNumber(entity.getCaseNumber());
        this.setBlock(entity.getBlock());
        this.setIUCR(entity.getIUCR());
        this.setLocationDescription(entity.getLocationDescription());
        this.setArrest(entity.isArrest());
        this.setCommunityArea(entity.getCommunityArea());
        this.setXCoord(entity.getXCoord());
        this.setYCoord(entity.getYCoord());
        this.setLatitude(entity.getLatitude());
        this.setLongitude(entity.getLongitude());
        this.uuid = new Uuid(entity.getUuid().getMostSignificantBits(), entity.getUuid().getLeastSignificantBits());
    }

    @Override
    public boolean isEqual(EntityCrimeRecord entity) {
        return ((this.getID() == entity.getID()) && //ID
                (this.getCaseNumber().equalsIgnoreCase(entity.getCaseNumber())) && // Case Number
                (this.getBlock().equalsIgnoreCase(entity.getBlock())) && //Block
                (this.getIUCR().equalsIgnoreCase(entity.getIUCR())) && //IUCR
                (this.getLocationDescription() == entity.getLocationDescription()) && //Location
                (this.isArrest() == entity.isArrest()) && //Arrest
                (this.getCommunityArea() == entity.getCommunityArea()) && // Community Area
                (this.getXCoord() == entity.getXCoord()) && //X Coordinate
                (this.getYCoord() == entity.getYCoord()) && //Y Coordinate
                (abs(this.getLatitude() - entity.getLatitude()) <= PRECISION) && //Latitude
                (abs(this.getLongitude() - entity.getLongitude()) <= PRECISION) //Longitude
                );
    }

